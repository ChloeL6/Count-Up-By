const countTo = 30; // input form for user 
const countBy = 3; // input form for user

function countUp(countTo, countBy) {
  let numArr = []; 
  let result = 0;

  for (let index = 1; result < countTo; index++) {
    result = countBy * index;
    numArr.push(result);
  }
  return numArr;
}

